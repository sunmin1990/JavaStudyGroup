package Study_workshop;

public class programmers {

	public static void main(String args[]) {
		int a=3;
		int b=5;
		long answer =0;
		
		
		for(int i=0; i<=b; i++) {
				answer += i;
				System.out.println(answer);
			}
		for(int j=0; j<=a; j++) {
				answer += j;
				System.out.println(answer);
			}
	}
}
