package Study_workshop;

public class Operator4 {

	public static void main(String[] args) {
		
		int r1 = 3+6; // int r =3+6; // int r = 3+6 // 다만 이렇게 썼을 경우에는 더하고 바로 출력하는 시스템이여야함. 
		int r2 = r1 +9; // r = r+9   //r +=9
		int r3 = r2 +12; //r = r+12  //r +=12
		
		System.out.println("3+6="+r1);
		System.out.println("3+6+9="+r2);
		System.out.println("3+6+9+12="+r3);
		
	}

}
